s = "Hello"
print(type(s))
# <class 'str'>

i = 3
print(type(i))
# <class 'int'>

f = 10.1
print(type(f))
# <class 'float'>

c = 3 + 4j
print(type(c))
# <class 'complex'>

t = (("apple","banana","cherry"))
print(type(t))
# <class 'tuple'>

l = list(("apple","banana","cherry"))
print(type(l))
# <class 'list'>

r = range(7)
print(type(r))
# <class 'range'>

d = dict(name="John",age=36)
print(type(d))
# <class 'dict'>

s = set(("apple","banana","cherry"))
print(type(s))
# <class 'set'>



